namespace Chance\CitrixRightSignature\ReusableTemplate;

use Chance\CitrixRightSignature\SendingRequest\Role;

class ReusableTemplateRole extends Role implements ReusableTemplateRoleInterface
{
    private $sequence = 0;

    private $message;

    /**
     * Specify data which should be serialized to JSON
     * @link https://php.net/manual/en/jsonserializable.jsonserialize.php
     * @return mixed data which can be serialized by <b>json_encode</b>,
     * which is a value of any type other than a resource.
     * @since 5.4.0
     */
    public function jsonSerialize()
    {
        $base = parent::jsonSerialize();

        $base['sequence'] = $this->sequence;
        $base['message'] = $this->message;

        return $base;
    }

    /**
     * @return int
     */
    public function getSequence()
    {
        return $this->sequence;
    }

    /**
     * @param int $sequence
     */
    public function setSequence($sequence)
    {
        $this->sequence = $sequence;
    }

    /**
     * @return mixed
     */
    public function getMessage()
    {
        return $this->message;
    }

    /**
     * @param mixed $message
     */
    public function setMessage($message)
    {
        $this->message = $message;
    }
}