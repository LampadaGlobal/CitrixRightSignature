namespace Chance\CitrixRightSignature\Token;

// https://api.rightsignature.com/documentation/resources/v1/oauth_tokens/create.en.html

class AccessToken implements AccessTokenInterface
{
    private $accessToken;

    private $tokenType;

    private $refreshToken;

    private $expiresIn;

    private $scope;

    private $createdAt;

    /**
     * Specify data which should be serialized to JSON
     * @link https://php.net/manual/en/jsonserializable.jsonserialize.php
     * @return mixed data which can be serialized by <b>json_encode</b>,
     * which is a value of any type other than a resource.
     * @since 5.4.0
     */
    public function jsonSerialize()
    {
        // TODO: Implement jsonSerialize() method.
        /*
         * {
    "access_token":  ACCESS_TOKEN,
    "token_type":    "bearer",
    "expires_in":    7200,
    "refresh_token": REFRESH_TOKEN,
    "scope":         "read",
    "created_at":    1569565521
  }
         */
    }

    /**
     * @return mixed
     */
    public function getAccessToken()
    {
        return $this->accessToken;
    }

    /**
     * @param mixed $accessToken
     */
    public function setAccessToken($accessToken)
    {
        $this->accessToken = $accessToken;
    }

    /**
     * @return mixed
     */
    public function getTokenType()
    {
        return $this->tokenType;
    }

    /**
     * @param mixed $tokenType
     */
    public function setTokenType($tokenType)
    {
        $this->tokenType = $tokenType;
    }

    /**
     * @return mixed
     */
    public function getRefreshToken()
    {
        return $this->refreshToken;
    }

    /**
     * @param mixed $refreshToken
     */
    public function setRefreshToken($refreshToken)
    {
        $this->refreshToken = $refreshToken;
    }

    /**
     * @return mixed
     */
    public function getExpiresIn()
    {
        return $this->expiresIn;
    }

    /**
     * @param mixed $expiresIn
     */
    public function setExpiresIn($expiresIn)
    {
        $this->expiresIn = $expiresIn;
    }

    /**
     * @return mixed
     */
    public function getScope()
    {
        return $this->scope;
    }

    /**
     * @param mixed $scope
     */
    public function setScope($scope)
    {
        $this->scope = $scope;
    }

    /**
     * @return mixed
     */
    public function getCreatedAt()
    {
        return $this->createdAt;
    }

    /**
     * @param mixed $createdAt
     */
    public function setCreatedAt($createdAt)
    {
        $this->createdAt = $createdAt;
    }

    /**
     * @param array $accessTokenArray
     * @return AccessTokenInterface
     */
    public static function createFromApiResponse(array $accessTokenArray)
    {
        /*
         * {
    "access_token":  ACCESS_TOKEN,
    "token_type":    "bearer",
    "expires_in":    7200,
    "refresh_token": REFRESH_TOKEN,
    "scope":         "read",
    "created_at":    1569824720
  }
         */

        $token = new static();
        $token->setAccessToken($accessTokenArray['access_token']);
        $token->setTokenType($accessTokenArray['token_type']);
        $token->setExpiresIn($accessTokenArray['expires_in']);
        $token->setRefreshToken($accessTokenArray['refresh_token']);
        $token->setScope($accessTokenArray['scope']);
        $token->setCreatedAt($accessTokenArray['created_at']);

        return $token;
    }
}